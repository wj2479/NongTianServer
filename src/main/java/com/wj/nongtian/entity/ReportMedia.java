package com.wj.nongtian.entity;

public class ReportMedia extends Medias {
    /**
     * 主键id
     */
    private int id;

    /**
     * 日报的ID
     */
    private int rid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRid() {
        return rid;
    }

    public void setRid(int rid) {
        this.rid = rid;
    }
}
